// let — CAN be reassigned later
let score = 10;
score = 20;        // ✅ works fine
console.log(score) // 20

// const — CANNOT be reassigned
const name = "Ravi";
// name = "Kumar";    // ❌ Error! Cannot reassign a const
console.log(name)




// NOTES
// let → value can change later
// const → value stays fixed forever